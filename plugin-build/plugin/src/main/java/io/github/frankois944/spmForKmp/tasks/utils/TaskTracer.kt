    @Suppress("MagicNumber")
    private fun renderRow(
        span: Span,
        totalMs: Long,
        depth: Int,
    ): String {
        val ms = span.durationMillis()
        val selfMs = span.selfDurationMillis()
        val pct = if (totalMs > 0) (ms.toDouble() / totalMs * 100).coerceIn(0.0, 100.0) else 0.0
        val pctLabel = String.format(locale = Locale.US, "%.1f", pct)

        val row =
            """
            <tr>
              <td class="col-span" style="--depth:$depth">
                <span class="span-name">
                  <span class="dot" aria-hidden="true"></span>
                  <span>${esc(span.name)}</span>
                  <span class="badge" title="Children">${span.children.size}</span>
                </span>
              </td>
              <td class="col-num">$ms</td>
              <td class="col-num">$selfMs</td>
              <td class="col-pct">
                <span class="pct-wrap">
                  <span class="bar" aria-hidden="true"><i style="width:$pctLabel%"></i></span>
                  <span class="pct-val">$pctLabel%</span>
                </span>
              </td>
              <td class="col-num">${span.children.size}</td>
            </tr>
            """.trimIndent()

        val children = span.children.joinToString("") { renderRow(it, totalMs, depth + 1) }
        return row + children
    }

    @Suppress("MagicNumber")
    private fun renderSpan(
        span: Span,
        totalMs: Long,
    ): String {
        val ms = span.durationMillis()
        val pct = if (totalMs > 0) (ms.toDouble() / totalMs * 100).coerceAtMost(100.0) else 0.0
        val barWidth = pct
        val childrenHtml =
            if (span.children.isNotEmpty()) {
                "<ul>" + span.children.joinToString("") { renderSpan(it, totalMs) } + "</ul>"
            } else {
                ""
            }
        return """
                                                                                                                                                                                    <li>
                                                                                                                                                                                      <div><strong>${
            esc(
                span.name,
            )
        }</strong> â€” $ms ms <span class=\"pct\">(${
            String.format(
                locale = Locale.US,
                "%.1f",
                pct,
            )
        }%)</span></div>
                                                                                                                                                                                      <div class=\"bar\" style=\"width:${
            String.format(
                locale = Locale.US,
                "%.1f",
                barWidth,
            )
        }%\"></div>
                                                                                                                                                                                      $childrenHtml
                                                                                                                                                                                    </li>
            """.trimIndent()
    }

    private fun Span.durationMillis(): Long {
        val endInstant = end ?: Instant.now().toEpochMilli()
        return endInstant - start
    }

    private fun Span.selfDurationMillis(): Long {
        val childrenMs = children.sumOf { it.durationMillis() }
        val total = durationMillis()
        return (total - childrenMs).coerceAtLeast(0)
    }

    private fun esc(s: String): String =
        s
            .replace("&", "&amp;")
            .replace("<", "&lt;")
            .replace(">", "&gt;")
            .replace("\"", "&quot;")

    private data class Span(
        val name: String,
        val start: Long,
        var end: Long? = null,
        val children: MutableList<Span> = mutableListOf(),
    )
}
